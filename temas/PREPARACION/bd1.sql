CREATE DATABASE FABRICAS
CHARACTER SET utf8 COLLATE utf8_general_ci;

USE FABRICAS; 

CREATE TABLE FABRICA ( 
IDEF CHAR(6) NOT NULL PRIMARY KEY, 
NOMBRE VARCHAR(35) DEFAULT ' ', 
TELEFONO INT(5) DEFAULT 0 
) ENGINE = InnoDB; 

CREATE TABLE EMPLEADO ( 
CODEMP CHAR(6) NOT NULL PRIMARY KEY, 
NOMBREEMP VARCHAR(35) DEFAULT ' ', 
IDEF CHAR(6),
FOREIGN KEY (IDEF) REFERENCES FABRICA(IDEF) 
) ENGINE = InnoDB; 

CREATE TABLE MAQUINA ( 
COD CHAR(6) NOT NULL PRIMARY KEY, 
DESCRIPCION VARCHAR(35) DEFAULT ' ', 
NSERIE INT(8) DEFAULT 0 
) ENGINE = InnoDB; 

CREATE TABLE ALMACEN (
COD CHAR(6) NOT NULL PRIMARY KEY, 
CENTRADA INT(4) DEFAULT 0, 
CSALIDA INT(4) DEFAULT 0,
CSALDO INT(4) DEFAULT 0, 
FOREIGN KEY (COD) REFERENCES MAQUINA(COD) 
) ENGINE = InnoDB; 

CREATE TABLE COMPRA ( 
COD CHAR(6) NOT NULL, 
CODEMP CHAR(6) NOT NULL, 
CANTIDADC INT(4) DEFAULT 0,
FECHAC DATETIME DEFAULT NOW(), 
PRIMARY KEY(COD, CODEMP),
FOREIGN KEY (COD) REFERENCES MAQUINA(COD),
FOREIGN KEY (CODEMP) REFERENCES EMPLEADO(CODEMP)
) ENGINE = InnoDB; 

CREATE TABLE DOTACION ( 
NDOT INT(4) NOT NULL PRIMARY KEY, 
CANTIDADDOT INT(4) DEFAULT 0, 
FECHADOT DATETIME DEFAULT NOW(), 
COD CHAR(6), 
CODEMP CHAR(6),
FOREIGN KEY (COD, CODEMP) REFERENCES COMPRA(COD, CODEMP) 
) ENGINE = InnoDB; 

